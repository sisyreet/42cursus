char	*ft_strrchr(const char *str, int ch)
{
	int	s;

	s = 0;
	while (str[s] != '\0')
		s++;
	s--;
	while (str[s] != '\0')
	{
		if (str[s] == ch)
			return ((char *) &str[s]);
		s--;
	}
	return (0);
}
